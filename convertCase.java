public class convertCase {
    public static void main(String[] args) {
        String upperCase = "HELLO WORLD";
        String lowerCase = "java";

        System.out.println("String: "+ upperCase + " convert to lowercase: " + upperCase.toLowerCase() );
        System.out.println("String: "+ lowerCase + " convert to uppercase: " + lowerCase.toUpperCase() );
    }
}
